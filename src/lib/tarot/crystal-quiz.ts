function pick<T>(arr: T[]): T { return arr[Math.floor(Math.random() * arr.length)] }

/* ─── Public API ──────────────────────────────────────────────── */

export function generateCrystalQuiz(
  cards: { cardName: string; orientation: "正位"|"逆位"; element: string; positionLabel: string }[],
  userName = "",
): QuizQuestion[] {
  const used: Set<string> = new Set()
  const result: QuizQuestion[] = []

  for (let i = 0; i < 3; i++) {
    const card = pick(cards)
    const archetype = MAJOR_MAP[card.cardName]
      || (card.element === "火" ? "wands"
        : card.element === "水" ? "cups"
        : card.element === "风" ? "swords"
        : card.element === "土" ? "pentacles"
        : "generic")
    const pool = Q[archetype] || Q.generic

    // Pick unused template, fallback to any
    let tmpl = ""
    for (let t = 0; t < 30; t++) {
      const c = pick(pool)
      if (!used.has(c)) { tmpl = c; used.add(c); break }
    }
    if (!tmpl) tmpl = pick(pool)

    const greeting = userName ? `${userName}，` : ""
    const q = tmpl
      .replace(/\{card\}/g, card.cardName)
      .replace(/\{position\}/g, card.positionLabel)
      .replace(/\{user\}/g, greeting)

    const opts = i === 0 ? pick(STATE_SETS)
      : i === 1 ? pick(NEED_SETS)
      : pick(HAND_SETS)

    result.push({ question: q, options: opts as QuizQuestion['options'] })
  }

  return result
}
